    # Verify correct queries were made
    mock_get_by_user_id.assert_called_once_with("user123", CredentialTypes.LITE_LLM)
    mock_get_by_project_names.assert_called_once_with(["project-1", "project-2"], CredentialTypes.LITE_LLM)


