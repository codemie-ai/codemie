@patch.object(Settings, 'get_by_user_id')
@patch.object(Settings, 'get_by_project_names')
def test_get_user_litellm_api_keys_returns_grouped_keys(mock_get_by_project_names, mock_get_by_user_id):
    """Test get_user_litellm_api_keys returns API keys grouped by USER and PROJECT."""
    # Given: USER-scoped LiteLLM settings
    user_setting1 = Settings(
        id="user-setting-1",
        user_id="user123",
        project_name="user-project",
        alias="user-litellm-1",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="api_key", value="user-api-key-1"),
            CredentialValues(key="url", value="https://litellm.example.com"),
        ],
        setting_type=SettingType.USER,
    )
    user_setting2 = Settings(
        id="user-setting-2",
        user_id="user123",
        project_name="user-project",
        alias="user-litellm-2",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="api_key", value="user-api-key-2"),
        ],
        setting_type=SettingType.USER,
    )

    # Given: PROJECT-scoped LiteLLM settings
    project_setting1 = Settings(
        id="project-setting-1",
        user_id=None,
        project_name="project-1",
        alias="project-litellm-1",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="api_key", value="project-api-key-1"),
        ],
        setting_type=SettingType.PROJECT,
    )

    mock_get_by_user_id.return_value = [user_setting1, user_setting2]
    mock_get_by_project_names.return_value = [project_setting1]

    # When: Getting user's LiteLLM API keys
    result = SettingsService.get_user_litellm_api_keys("user123", ["project-1", "project-2"])

    # Then: Keys are grouped by type
    assert "user_keys" in result
    assert "project_keys" in result
    assert len(result["user_keys"]) == 2
    assert "user-api-key-1" in result["user_keys"]
    assert "user-api-key-2" in result["user_keys"]
    assert len(result["project_keys"]) == 1
    assert "project-api-key-1" in result["project_keys"]

    # Verify correct queries were made
    mock_get_by_user_id.assert_called_once_with("user123", CredentialTypes.LITE_LLM)
    mock_get_by_project_names.assert_called_once_with(["project-1", "project-2"], CredentialTypes.LITE_LLM)


@patch.object(Settings, 'get_by_user_id')
@patch.object(Settings, 'get_by_project_names')
def test_get_user_litellm_api_keys_deduplicates_by_setting_id(mock_get_by_project_names, mock_get_by_user_id):
    """Test that settings with same ID are deduplicated."""
    # Given: Settings with duplicate IDs
    user_setting = Settings(
        id="same-setting-id",
        user_id="user123",
        project_name="user-project",
        alias="litellm-setting",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="api_key", value="api-key-1"),
        ],
        setting_type=SettingType.USER,
    )

    project_setting = Settings(
        id="same-setting-id",  # Same ID - should be deduplicated
        user_id=None,
        project_name="project-1",
        alias="litellm-setting",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="api_key", value="api-key-1"),
        ],
        setting_type=SettingType.PROJECT,
    )

    mock_get_by_user_id.return_value = [user_setting]
    mock_get_by_project_names.return_value = [project_setting]

    # When: Getting user's LiteLLM API keys
    result = SettingsService.get_user_litellm_api_keys("user123", ["project-1"])

    # Then: Only one key is included (deduplicated)
    total_keys = len(result["user_keys"]) + len(result["project_keys"])
    assert total_keys == 1


@patch.object(Settings, 'get_by_user_id')
@patch.object(Settings, 'get_by_project_names')
def test_get_user_litellm_api_keys_skips_settings_without_api_key(mock_get_by_project_names, mock_get_by_user_id):
    """Test that settings without api_key field are skipped."""
    # Given: Setting without api_key field
    setting_no_key = Settings(
        id="setting-no-key",
        user_id="user123",
        project_name="user-project",
        alias="litellm-no-key",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="url", value="https://litellm.example.com"),
            CredentialValues(key="other", value="value"),
        ],
        setting_type=SettingType.USER,
    )

    # Given: Valid setting with api_key
    setting_with_key = Settings(
        id="setting-with-key",
        user_id="user123",
        project_name="user-project",
        alias="litellm-with-key",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="api_key", value="valid-api-key"),
        ],
        setting_type=SettingType.USER,
    )

    mock_get_by_user_id.return_value = [setting_no_key, setting_with_key]
    mock_get_by_project_names.return_value = []

    # When: Getting user's LiteLLM API keys
    result = SettingsService.get_user_litellm_api_keys("user123", [])

    # Then: Only the setting with api_key is included
    assert len(result["user_keys"]) == 1
    assert "valid-api-key" in result["user_keys"]
    assert len(result["project_keys"]) == 0


