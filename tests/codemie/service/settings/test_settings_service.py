        id="user-setting-2",
        user_id="user123",
        project_name="user-project",
        alias="user-litellm-2",
        credential_type=CredentialTypes.LITE_LLM,
        credential_values=[
            CredentialValues(key="api_key", value="user-api-key-2"),
        ],
        setting_type=SettingType.USER,
