@patch.object(Settings, 'get_by_user_id')
def test_get_settings_filters_out_ide_internal_settings(
    mock_get_by_user_id, regular_user_setting, ide_internal_setting
):
    """Test that internal IDE settings are filtered out from user settings."""
    # Given: Database returns both regular and internal IDE settings
    mock_get_by_user_id.return_value = [regular_user_setting, ide_internal_setting]

    # When: Getting user settings
    result = SettingsService.get_settings(
        user_id="user123", project_names=None, settings_type=None, credential_type=None
    )

    # Then: Only regular setting is returned, IDE setting is filtered out
    assert len(result) == 1
    assert result[0].alias == "my-jira-integration"
    assert not result[0].alias.startswith(SettingsService.INTERNAL_PREFIX)
    mock_get_by_user_id.assert_called_once_with("user123", credential_type=None)


